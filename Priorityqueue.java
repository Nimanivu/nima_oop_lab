import java.util.*;    
class Priorityqueue {    
    public static void main(String args[]) {    
        // Creating a PriorityQueue of Strings  
        PriorityQueue<Integer> qu = new PriorityQueue<Integer>();    
        // Adding elements to the PriorityQueue  
        qu.add(39);    
        qu.add(19);    
        qu.add(89);    
        qu.offer(29);    
        qu.offer(67);    
        
       System.out.println(qu);
       }
       }
